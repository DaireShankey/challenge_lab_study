# View commit history with details
git log
# Shows the commit history with commit hashes, authors, dates, and messages.

# View history in a concise, single-line format
git log --oneline
# Shows a simplified view of the commit history.

# Check the status of your working directory and staging area
git status
# Shows which files are staged, modified, or untracked.

# View differences between working directory and staging area
git diff
# Displays unstaged changes in the working directory.

# View differences between the staging area and the last commit
git diff --staged
# Shows changes that are staged for the next commit.
