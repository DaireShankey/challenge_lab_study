# loops.py

# For loop: print numbers 1 to 5
print("For loop example:")
for i in range(1, 6):
    print(i)
# `range(1, 6)` generates numbers from 1 to 5.

# While loop: print numbers 5 to 1
print("While loop example:")
count = 5
while count > 0:
    print(count)
    count -= 1
# `count -= 1` decreases count by 1 in each iteration.
